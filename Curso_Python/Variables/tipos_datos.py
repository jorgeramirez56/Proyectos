# Ejemplo de tipos de datos en Python 

# Entero 
edad = 23 
print('Edad: ', edad)
altura = 1.53
print('Altura: ', altura)

# Cadena de texto 
nombre = 'Ana'
print('Nombre:', nombre)

# Tipo boolean 
es_estudiante = False
print("es_estudiante?", es_estudiante)

# None 
direccion = None
print("Direccion: ", direccion)
