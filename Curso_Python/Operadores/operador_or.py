print('*** Operador logico or ***')
print('Primer caso:')
condicion1 = False
condicion2 = False
# El operador or regresa True si cualquiera de los operandos es True
resultado = condicion1 or condicion2
print(f'El resultado de {condicion1} or {condicion2} es: {resultado}')

print('Segundo caso:')
condicion1 = True
condicion2 = False
# El operador or regresa True si cualquiera de los operandos es True
resultado = condicion1 or condicion2
print(f'El resultado de {condicion1} or {condicion2} es: {resultado}')

print('Segundo caso:')
condicion1 = True
condicion2 = True
# El operador or regresa True si cualquiera de los operandos es True
resultado = condicion1 or condicion2
print(f'El resultado de {condicion1} or {condicion2} es: {resultado}')