print('*** Operadores de comparacion ***')
a, b = 7, 5
print(f'Valor inicial a: {a}, b: {b}')

# Operador igualdad ==
resultado = a == b
print(f'El resultado de a == b es: {resultado}')

# Operador diferente !=
resultado = a != b
print(f'Resultado a != b es: {resultado}')

# Operador mayor que > 
resultado = a > b
print(f'Resultado a > b es: {resultado}')

# Operador mayor o igual >=
resultado = a >= b
print(f'Rasultado a >= b es: {resultado}')

# Operador menor que < 
resultado = a < b
print(f'Resultado a < b es: {resultado}')

# Operador menor o igual <=
resultado = a <= b
print(f'Resultado a <= b es: {resultado}\n\n')




