# Operadores Aritmeticos 
a = 10 
b = 3 

# Suma 
suma = a + b
print(f'El resultado de la suma es: {suma}')

# Resta
resta = a - b 
print(f'El resultado de la resta es: {resta}')

# Multiplicacion 
multiplicacion = a * b
print(f'El resultado de la multiplicacion es: {multiplicacion}')

# Division /

division = a / b
print(f'El resultado de la division es: {division:.2f}')

# Division entera (//)
division_entera = a // b
print(f'El resultado de la division entera es: {division_entera}')

# Modulo (%) residuo de la division 
modulo = a % b
print(f'El residuo de la division es: {modulo}')

# Exponente (potencia) **
exponente = a ** b # 10 elevado a la 3 

print(f'El resultado de la potencia es: {exponente}')

