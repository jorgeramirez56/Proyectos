# Concatenacion de cadenas
cadena1 = 'Hola'
cadena2 = 'Mundo'
concatenacion = cadena1 + cadena2  #concatenacion = 'Hola' + 'Mundo'
print(concatenacion)

concatenacion = cadena1 + ' ' + cadena2
print(concatenacion)

# Utilizando el metodo join

concatenacion = ''.join([cadena1, ' ', cadena2])
print(concatenacion)